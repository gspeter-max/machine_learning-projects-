import math 

def potential_embiding(sen_len , embiding_dim): 
    result = [[0 for _ in range(embiding_dim)] for _ in range(sen_len)]

    for pos in range(sen_len):
        for i in range(embiding_dim): 

            angle = pos / 10000**(2*(i//2)/embiding_dim)
            if i % 2 == 0 : 
                result[pos][i] = math.sin(angle)
            else:
                result[pos][i] = math.cos(angle)
    return result

result = potential_embiding(10,4)
for row in result: 
    print(row)
